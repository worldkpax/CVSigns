def resolve_rtsd_sign_name(raw_name: str | None, class_id: int) -> str | None:
    normalized = normalize_rtsd_code(raw_name)
    if normalized and normalized in RTSD_SIGN_NAMES_RU:
        return RTSD_SIGN_NAMES_RU[normalized]

    if _looks_like_class_index(raw_name, class_id) and 0 <= class_id < len(RTSD_CLASS_CODES):
        return RTSD_SIGN_NAMES_RU.get(RTSD_CLASS_CODES[class_id])

    if 0 <= class_id < len(RTSD_CLASS_CODES):
        fallback_code = RTSD_CLASS_CODES[class_id]
        return RTSD_SIGN_NAMES_RU.get(fallback_code)

    return None


def normalize_rtsd_code(value: str | None) -> str | None:
    if value is None:
        return None
    normalized = value.strip().replace(".", "_")
    if not normalized:
        return None
    return normalized


def _looks_like_class_index(raw_name: str | None, class_id: int) -> bool:
    if raw_name is None:
        return True
    normalized = raw_name.strip().lower()
    return normalized in {"", str(class_id), f"class_{class_id}"}
